package com.golinocottibeatrice.kernelsearch.util;

/**
 * Rappresenta una coppia di valori interi.
 */
public class Pair {
    private final int j;
    private final int k;

    public Pair(int j, int k) {
        this.j = j;
        this.k = k;
    }

    public int getJ() {
        return j;
    }

    public int getK() {
        return k;
    }
}

